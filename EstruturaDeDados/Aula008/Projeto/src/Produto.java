public class Produto {
	public String nome;
	public int codigo;
	public int quantidade;
	public float preco;
	
	public Produto() {
		
	}
	
	public Produto(String nome, int codigo, int quantidade, float preco) {
		this.nome = nome;
		this.codigo = codigo;
		this.quantidade = quantidade;
		this.preco = preco;
	}

}